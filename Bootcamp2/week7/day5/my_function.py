def my_name_function(first_name, last_name):
    """This function prints the full name"""

    full_name = first_name + " " + last_name

    return full_name


full_name_1 = my_name_function("Sarah", "Haq")
print(f"My full name is {full_name_1}")
full_name_2 = my_name_function("Bob", "Marley")
print(f"My full name is {full_name_2}")
full_name_3 = my_name_function("Charlie", "Chaplin")
print(f"My full name is {full_name_3}")